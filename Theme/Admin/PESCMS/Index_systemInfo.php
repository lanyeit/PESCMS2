    <h2 class="main-title">为什么要写这模版</h2>
</header>
<p>1.网上找了不是后台模版，下载安装后都觉得里面杂陈了太多东西了。没心思惊醒下来精简</p>
<p>2.模版怎么没用到前端框架。我用过bootstrap，但本人不喜欢。bootstrap太重了，不符合我的风格。</p>
<p>3.模板的设计来自参照各种国外优秀的后台模版而来的，也许你会发现有一种似曾相识的味道。</p>
<p>4.模版注释我都写好的，你需要看一下源码就懂了。我个人认为此模版合适像我这么狂野的人使用。</p>
<p>5.本模版不允许闭源的商业软件使用。就是说，你闭源了不商业没问题。你商业使用，你的软件也必须开源。</p>
<p>6.模版目前存在一些BUG，如果您有解决的方法，请PUSH到项目的GIT去，谢了。</p>
