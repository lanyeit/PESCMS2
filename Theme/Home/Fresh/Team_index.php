                    <p data-anchor-id="v5fz">1.直接访问您的目录，程序将自动为您指向安装界面（没有则自行补上http://域名/Install/index.php）。 <br>
                        2.安装完毕后，程序将自行移除一些不必要文件，避免重复安装等问题。 <br>
                        3.直接访问http://域名/，程序将自动检测您是否登录。 <br>
                        4.若需要邮件通知功能，请到系统--系统设置中填写您的smtp邮箱配置。  </p>

